# You can convert a float to an int.

x = 3.7
print(int(x))

# You can convert a string to a float.

y = '97.3'
print(float(y) + 2)

# You can convert an int to a string.

z = 3
print('one possible number is ' + str(z))

# Usually we wouldn't concatenate strings like that, though. We'd use

print('one possible number is %s' % z)


