# We can use double quotes or single quotes.

r = 'Single quote string.'
s = "Double quote string."

print(r)
print(s)

# A len function allows us to get the length of a string.

r = 'abc'
print(len(r))
s = 'abcdefghijklmnopqrstuvwxyz'
print(len(s))

# You can also have multi-line strings.

multi_line = '''Some really long string.
It can take up multiple lines.
The multi-line comment is really just a string and that's just valid syntax.'''
print(multi_line)

