# To use a variable, we just give it a name and assign it a value.

number = 4

print(number)

# Variable names are case sensitive.

value = 50
Value = 50

print(value)
print(Value)

# Variables can also be used to perform calculations

number_five = 5
one_more_than_five = number_five + 1
print(one_more_than_five)

