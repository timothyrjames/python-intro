# Creating a dictionary.
dictionary = {}
print('This is an empty dictionary:')
print(dictionary)

# Another (less common) way to create a dictionary.
d = dict()
print('This is another empty dictionary:')
print(d)

states = {}
# Populating a dictionary.
states['NY'] = 'New York'
states['CA'] = 'California'

print('A dictionary with values:')
print(states)

